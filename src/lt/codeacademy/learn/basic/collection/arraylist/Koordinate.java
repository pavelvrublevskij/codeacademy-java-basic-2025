package lt.codeacademy.learn.basic.collection.arraylist;

public class Koordinate {
	private int x;
	private int y;

	public Koordinate(final int x, final int y) {
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(final int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(final int y) {
		this.y = y;
	}
}
